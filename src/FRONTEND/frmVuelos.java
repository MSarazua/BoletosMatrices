    private void tbVueloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbVueloMouseClicked
        // TODO add your handling code here:
        try{
            int fila = tbVuelo.getSelectedRow();
            int Codigo =   Integer.parseInt(  tbVuelo.getValueAt(fila, 0).toString()    )  ;
            ResultSet rs;
            clsQuerys objBD = new clsQuerys();
            rs = objBD.fncConsultaVuelos(Codigo);
            
            while(rs.next()){
                ctId.setText( rs.getString("Id")  );
                cbOrigen.setSelectedItem(rs.getString("Origen"));
                cbDestino.setSelectedItem(rs.getString("Destino"));
                ctFecha.setText(rs.getString("Fecha"));
                cbHoraSalida.setSelectedItem(rs.getString("HoraSalida"));
                cbHoraLlegada.setSelectedItem(rs.getString("HoraLlegada"));
                cbTipo.setSelectedItem(rs.getString("TipoAvion")  );
                cbEstatus.setSelectedItem(rs.getString("Estatus"));
            }
         
        }catch(Exception Ex){
            System.out.println( Ex.getMessage());
        }
    }//GEN-LAST:event_tbVueloMouseClicked

