    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if(fncValidaFormulario()==1){
            clsVuelos objVuelos = new clsVuelos();

            objVuelos.setOrigen(cbOrigen.getSelectedItem().toString()  );
            objVuelos.setDestino(cbDestino.getSelectedItem().toString()  );
            objVuelos.setFecha(  ctFecha.getText()   );
            objVuelos.setHoraSalida(cbHoraSalida.getSelectedItem().toString()  );
            objVuelos.setHoraLlegada(cbHoraLlegada.getSelectedItem().toString());
            objVuelos.setTipoAvion(cbTipo.getSelectedItem().toString()   );
            objVuelos.setEstatus(cbEstatus.getSelectedItem().toString());
             clsQuerys objInsertar = new clsQuerys();  //CREA UN INSTANCIA

             if(  objInsertar.fncIngresoVuelo(objVuelos)==1){
                    JOptionPane.showMessageDialog(this, "VUELO REGISTRADO.");
                     CargaVuelos();
                     Limpiar();
                    
             }else{
                    JOptionPane.showMessageDialog(this, "VUELO NO REGISTRADO.");   
             }
            }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(fncValidaFormulario()==1){
            if(ctId.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Debe seleccionar un registro para actualizar");
            }else{
                clsVuelos objVuelo = new clsVuelos();
                objVuelo.setId(Integer.valueOf(  ctId.getText()    ) );
                objVuelo.setOrigen(cbOrigen.getSelectedItem().toString()  );
                objVuelo.setDestino(cbDestino.getSelectedItem().toString()  );
                objVuelo.setFecha(  ctFecha.getText()   );
                objVuelo.setHoraSalida(cbHoraSalida.getSelectedItem().toString()  );
                objVuelo.setHoraLlegada(cbHoraLlegada.getSelectedItem().toString());
                objVuelo.setTipoAvion(cbTipo.getSelectedItem().toString()  );
                objVuelo.setEstatus(cbEstatus.getSelectedItem().toString());
                    

                clsQuerys objBD = new clsQuerys();  //CREA UN INSTANCIA

                if(  objBD.fncModificaCliente(objVuelo)==1){
                    JOptionPane.showMessageDialog(this, "Registro fue Actualizado.");
                    CargaVuelos();
                    Limpiar();
                }else{
                    JOptionPane.showMessageDialog(this, "Registro NO Ingresado.");
                }
            }
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        clsQuerys objBD = new clsQuerys();
        if(ctId.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe seleccionar un registro para eliminar");
        }else{
            if (    objBD.fncEliminaVuelo( Integer.valueOf(  ctId.getText()    ) ) ==1 ){
                JOptionPane.showMessageDialog(this, "Se eliminó correctamente el vuelo.");
                CargaVuelos();
                Limpiar();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

