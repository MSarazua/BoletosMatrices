    private void CargaVuelos(){
        DefaultTableModel modeloTabla =  (DefaultTableModel)tbVuelo.getModel();
        modeloTabla.setRowCount(0); //ELIMINO FILAS
        ResultSet rs;  //BD
        ResultSetMetaData rsmd;  // TABLA
        int Columnas;
        try{  
            clsQuerys objBD = new clsQuerys();
            rs = objBD.fncConsultaVuelos(0);
            rsmd = rs.getMetaData();
            Columnas = rsmd.getColumnCount();
            
            while(rs.next()){
                Object[] fila = new Object[Columnas];
                
                for(int indice = 0; indice < Columnas; indice ++){
                    
                    fila[indice] = rs.getObject(indice + 1 );
                }
                modeloTabla.addRow(fila);
                
            }
                    
                    
        }catch(SQLException e){
            System.out.println(e.toString());
        }
        
        
    }
    

